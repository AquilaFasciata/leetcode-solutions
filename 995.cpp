class Solution {
 private:
  static void flipSubArr(std::vector<int>& vec, int startIndex,
                         int placesToSwap, int& flips) {
    for (int i = 0; i < placesToSwap; i++) {
      vec.at(startIndex + i) = !vec.at(startIndex + i);
      if (i + 1 > vec.size()) {
        break;
      }
    }
  }

 public:
  static int minKBitFlips(std::vector<int>& nums, int k) {
    int numFlips = 0;
    for (int i = 0; i < nums.size(); i++) {
      if (nums[i] == 0) {
        try {
          numFlips++;
          flipSubArr(nums, i, k, numFlips);
        } catch (const std::exception& e) {
          return -1;
        }
      }
    }
    return numFlips;
  }
};

