#include <ctime>
#include <iostream>
#include <vector>

class Solution {
 public:
  static int minKBitFlips(std::vector<int>& nums, int k) {
    std::vector<int> flipQueue;
    int numFlips = 0;
    for (int i = 0; i < nums.size(); i++) {
      while (flipQueue.empty() == false && i > flipQueue[0] + k - 1) {
        flipQueue.erase(flipQueue.begin());
      }
      if ((nums[i] + flipQueue.size()) % 2 == 0) {
        if (i + k > nums.size()) {
          return -1;
        }
        numFlips++;
        flipQueue.push_back(i);
      }
    }
    return numFlips;
  }
};

int main() {
  std::vector<int> vec{0, 1, 0};
  clock_t startTime = clock();
  clock_t endTime;
  std::cout << startTime << '\n';
  int totalFlips = Solution::minKBitFlips(vec, 1);
  endTime = clock();
  std::cout << ((float)(endTime - startTime)) / CLOCKS_PER_SEC << '\n';
  std::cout << totalFlips << '\n';
  return 0;
}
